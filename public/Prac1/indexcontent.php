<h2>About Us</h2>
<p>When starting out in your home horticulture journey, it can be difficult navigating the plethora of plant species available, not to mention potentially dangerous.</p>
<h3>Plantifier simplifies gardening</h3>
<p>Plantifier is an app that identifies plants and gives care information based on a photo the user has taken. Plan identification is mediated by other users as training data for AI to then identify. Training data can be collected through a training mode where users can photograph and ID known plants.</p>
